"use strict";
function city_country(city, country) {
    return `${city}, ${country}`;
}
const city1 = city_country("Lahore", "Pakistan");
const city2 = city_country("Barcelona", "Spain");
const city3 = city_country("Toronto", "Canada");
console.log(city1);
console.log(city2);
console.log(city3);
